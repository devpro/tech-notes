function startWithSameWord(a: string, b: string): boolean {
  if (a == null || b == null) {
    return false
  }

  const aFirst = a.split('/').pop()?.split('-')[0].trim();
  const bFirst = b.split('/').pop()?.split('-')[0].trim();
  return aFirst === bFirst;
}

function sortSidebarItems(items: NormalizedSidebar): NormalizedSidebar {
  const sortItems = (items: NormalizedSidebar): NormalizedSidebar => {
    const sortedItems = [...items].sort((a, b) => {
      if (a.type === 'category' || b.type === 'category') {
        return 1
      }

      // remove file extension (otherwise "foo.md" would come after "foo-bar.md")
      const aId = ('id' in a ? a.id : '').replace('.md', '');
      const bId = ('id' in b ? b.id : '').replace('.md', '');

      if (!startWithSameWord(aId, bId)) {
        return 1;
      }

      return aId.localeCompare(bId);
    });

    return sortedItems.map((item: NormalizedSidebarItem) => {
      if (item.type === 'category') {
        return { ...item, items: sortItems(item.items) };
      }
      return item;
    }).filter((item: NormalizedSidebarItem) => {
      return item.type !== 'category' || item.label !== 'archive';
    });
  };

  return sortItems(items);
}

